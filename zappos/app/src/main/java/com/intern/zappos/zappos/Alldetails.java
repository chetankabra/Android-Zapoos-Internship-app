public class  Alldetails {
    String brandName;
    String originalPrice;
    String price;
    String imageUrl;
    String asin;
    String productRating;
    String productName;


public Alldetails(String brandName, String originalPrice, String price, String imageUrl, String asin, String productRating, String productName) {
        this.brandName = brandName;
        this.originalPrice = originalPrice;
        this.price = price;
        this.imageUrl = imageUrl;
        this.asin = asin;
        this.productRating = productRating;
        this.productName = productName;
    }

}

